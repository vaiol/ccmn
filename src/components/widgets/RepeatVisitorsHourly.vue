<template>
  <LineChart :data="chartData" :text="text" />
</template>

<script>
import LineChart from "@/components/charts/LineChart";
import api from "@/api/presence";
import {
  TIMES_LABELS,
  TIMES,
  BORDER_COLORS,
  BACKGROUND_COLORS
} from "@/constants";

export default {
  components: {
    LineChart
  },
  data: () => ({
    labels: TIMES_LABELS,
    data: [],
    text: "Repeat Visitors"
  }),
  props: ["params", "interval"],
  watch: {
    async params() {
      await this.getData();
    }
  },
  computed: {
    chartData() {
      return {
        labels: this.labels,
        datasets: TIMES.map((time, index) => ({
          label: TIMES_LABELS[index],
          backgroundColor: BORDER_COLORS[index],
          borderColor: BACKGROUND_COLORS[index],
          data: this.data[time]
        }))
      };
    },
    methods: {
      async getData() {
        const { labels, data } = await api.getRepeatVisitors(
          this.params,
          this.interval
        );
        this.labels = labels;
        this.data = data;
      }
    }
  }
};
</script>
